void	ft_ultimate_div_mod(int *a, int *b)
{
	int	a_cpy;
	int	b_cpy;

	a_cpy = *a;
	b_cpy = *b;
	*a = a_cpy / b_cpy;
	*b = a_cpy % b_cpy;
}
